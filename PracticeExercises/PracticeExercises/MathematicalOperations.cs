
using System;

namespace PracticeExercises
{
    public class MathematicalOperations
    {
        public int Multiplication(int _firstnumber, int _secondnumber)
        {
            return _firstnumber * _secondnumber;
        }
        public int Division(int _firstnumber, int _secondnumber)
        {
            return _firstnumber / _secondnumber;
        }
    }
}